# Program      : Lambda Filter Odd Demo
# Input        : [1, 2, 3, 4, 5, 6]
# Output       : [1, 3, 5]
# Description  : Lambda function using filter() to return odd numbers.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

numbers = [1, 2, 3, 4, 5, 6]
odds = list(filter(lambda x: x % 2 != 0, numbers))
print(odds)

