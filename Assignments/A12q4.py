# Program      : Print N Natural Numbers Demo
# Input        : 5
# Output       : 1 2 3 4 5
# Description  : Accepts one number and prints that many numbers starting from 1.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

def PrintNumbers(n):
    for i in range(1, n + 1):
        print(i, end=" ")

PrintNumbers(5)

