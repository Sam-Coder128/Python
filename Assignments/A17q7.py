# Program      : Number Pattern Demo
# Input        : 5
# Output       : 5 rows of 1 to 5
# Description  : Prints repeated number pattern.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

def Pattern(n):
    for i in range(n):
        for j in range(1, n+1):
            print(j, end=" ")
        print()

Pattern(5)

