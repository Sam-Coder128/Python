# Program      : Print N Numbers in Reverse Demo
# Input        : 5
# Output       : 5 4 3 2 1
# Description  : Accepts one number and prints that many numbers in reverse order.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

def PrintReverse(n):
    for i in range(n, 0, -1):
        print(i, end=" ")

PrintReverse(5)

