# Program      : Lambda Filter Even Demo
# Input        : [1, 2, 3, 4, 5, 6]
# Output       : [2, 4, 6]
# Description  : Lambda function using filter() to return even numbers.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

numbers = [1, 2, 3, 4, 5, 6]
evens = list(filter(lambda x: x % 2 == 0, numbers))
print(evens)

