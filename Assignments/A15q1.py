# Program      : Lambda Map Square Demo
# Input        : [1, 2, 3, 4]
# Output       : [1, 4, 9, 16]
# Description  : Lambda function using map() to return squares of list elements.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

numbers = [1, 2, 3, 4]
squares = list(map(lambda x: x * x, numbers))
print(squares)

