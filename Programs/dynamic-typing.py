# Program      : Dynamic Typing Demo
# Input        : 5 , 5.5 , Python
# Output       : <class 'int'> , <class 'float'> , <class 'str'>
# Description  : Demonstrates dynamic typing in Python.
# Author       : Samruddh Shivkumar Birajdar
#
############################################################################################################################

a = 5
print(type(a))

a = 5.5
print(type(a))

a = "Python"
print(type(a))

# Output:
# <class 'int'>
# <class 'float'>
# <class 'str'>

'''
Feature Demonstrated: Dynamic Typing

The same variable can refer to different types of values.

a → int
a → float
a → string

No explicit declaration is required.
'''

